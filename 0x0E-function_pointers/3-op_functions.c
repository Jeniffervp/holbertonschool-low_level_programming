#include "3-calc.h"

/**
 *op_add - Option to add
 * @a: a number.
 * @b: a number.
 *
 * Return: a + b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - Option to sub
 * @a: a number.
 * @b: a number.
 *
 * Return: a - b.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - Option to mul
 * @a: a number.
 * @b: a number.
 *
 * Return: a * b.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - Option to div
 * @a: a number.
 * @b: a number.
 *
 * Return: a * b.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - Option to mod
 * @a: a number.
 * @b: a number.
 *
 * Return: a * b.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
